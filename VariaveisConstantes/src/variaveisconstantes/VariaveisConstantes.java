package variaveisconstantes;
public class VariaveisConstantes {

    public static void main(String[] args) {
        // Constantes
        
        double pi = 3.14;
        
        System.out.println(pi);
        
        pi = 25.5;
        
        System.out.println(pi);
        
        pi = 10;
        
        System.out.println(pi);
       
        
        
    }
    
}
